#include<stdio.h>

int main(){
        //Declaration statement
        int a,b,sum;

        //Assignment statement
        a=100;
        b=50;

        //Expression statement
        sum=a+b;

        //Function statement
        printf("sum=%d\n",sum);

        //Control statement
        if(sum>140){
            printf("sum is greater 140\n");
        }

        //Return statement
        return 0;

}
